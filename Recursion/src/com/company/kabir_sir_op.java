package com.company;

import java.util.Scanner;

public class kabir_sir_op {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        String s= "";
        for (int i = 0;i<n;i++) {
            s=s+"* ";
            System.out.println(s);
        }
    }
}
